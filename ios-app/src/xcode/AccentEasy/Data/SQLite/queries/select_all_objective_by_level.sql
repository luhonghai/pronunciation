SELECT O.ID, O.NAME, O.DESCRIPTION, O.DATECREATED
FROM
COURSE AS C
INNER JOIN COURSEMAPPINGLEVEL as CML on C.ID = CML.IDCOURSE
    INNER JOIN LEVEL AS L ON L.ID = CML.IDLEVEL
    INNER JOIN COURSEMAPPINGDETAIL as CMD on CMD.IDLEVEL = CML.IDLEVEL
    INNER JOIN objective as O on O.ID = CMD.IDCHILD
WHERE C.ID = ? AND CMD.IDLEVEL = ? AND CMD.ISTEST = '\0'
ORDER BY CMD.[INDEX] ASC