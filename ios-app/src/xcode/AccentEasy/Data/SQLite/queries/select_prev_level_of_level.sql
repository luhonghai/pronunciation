SELECT L.ROWID AS _id,L.id, L.NAME, L.ISDEMO, L.DESCRIPTION, L.DATECREATED, L.COLOR, L.ISDEFAULTACTIVATED
FROM
  countrymappingcourse AS CMC
    INNER JOIN COURSE AS C ON C.ID = CMC.IDCOURSE
    INNER JOIN COUNTRY AS CT ON CT.ID = CMC.IDCOUNTRY
    INNER JOIN coursemappinglevel AS CML ON CMC.IDCOURSE = CML.IDCOURSE
    INNER JOIN level AS L ON L.ID = CML.IDLEVEL
WHERE CMC.IDCOUNTRY = ? AND CML.[index] < (
  SELECT CML.[index]
  FROM countrymappingcourse AS CMC
    INNER JOIN COURSE AS C ON C.ID = CMC.IDCOURSE
    INNER JOIN COUNTRY AS CT ON CT.ID = CMC.IDCOUNTRY
    INNER JOIN coursemappinglevel AS CML ON CMC.IDCOURSE = CML.IDCOURSE
    INNER JOIN level AS L ON L.ID = CML.IDLEVEL
  WHERE CMC.IDCOUNTRY = ? AND L.ID = ?
)
ORDER BY CML.[index] DESC LIMIT 1;