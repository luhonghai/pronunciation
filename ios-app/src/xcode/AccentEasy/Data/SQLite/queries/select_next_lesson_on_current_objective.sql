SELECT * FROM (LC.ID, LC.NAME, LC.DESCRIPTION, LC.TITLE, LC.DATECREATED, OM.[INDEX]
FROM
COURSE AS C
INNER JOIN COURSEMAPPINGLEVEL as CML on C.ID = CML.IDCOURSE
    INNER JOIN LEVEL AS L on L.ID = CML.IDLEVEL
    INNER JOIN COURSEMAPPINGDETAIL as CMD on CMD.IDLEVEL = CML.IDLEVEL
    INNER JOIN objectivemapping AS OM on OM.IDOBJECTIVE = CMD.IDCHILD
    INNER JOIN lessoncollection AS LC ON LC.ID = OM.IDLESSONCOLLECTION
WHERE C.ID = ? AND CMD.IDLEVEL = ? AND CMD.IDCHILD = ? AND CMD.ISTEST = '\0'
ORDER BY OM.[INDEX] ASC) AS TMP WHERE TMP.[INDEX] > (
  SELECT
    OM.[INDEX]
      FROM
COURSE AS C
INNER JOIN COURSEMAPPINGLEVEL as CML on C.ID = CML.IDCOURSE
      INNER JOIN LEVEL AS L on L.ID = CML.IDLEVEL
      INNER JOIN COURSEMAPPINGDETAIL as CMD on CMD.IDLEVEL = CML.IDLEVEL
      INNER JOIN objectivemapping AS OM on OM.IDOBJECTIVE = CMD.IDCHILD
      INNER JOIN lessoncollection AS LC ON LC.ID = OM.IDLESSONCOLLECTION
      WHERE C.ID = ? AND CMD.IDLEVEL = ? AND CMD.IDCHILD = ? AND CMD.ISTEST = '\0'
    AND LC.ID = ? LIMIT 1
) LIMIT 1